CREATE TABLE Plans(
    `PLAN_ID` INT PRIMARY KEY NOT NULL AUTO_INCREMENT,
    `PlAN_NAME` VARCHAR(20),
    `PlAN_VALUE` DECIMAL NOT NULL
) engine = InnoDB;

